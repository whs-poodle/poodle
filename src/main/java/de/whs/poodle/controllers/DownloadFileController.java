package de.whs.poodle.controllers;

import java.io.IOException;

import javax.servlet.http.HttpServletResponse;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

import de.whs.poodle.repositories.FileRepository;

/*
 * Used to download a particular file (attachments etc.).
 */
@Controller
@RequestMapping("download")
public class DownloadFileController {

	@Autowired
	private FileRepository fileRepo;

	@RequestMapping(value = "/{fileId}", method = RequestMethod.GET)
	public void downloadFile(@PathVariable int fileId, HttpServletResponse response) throws IOException {
		fileRepo.writeFileToHttpResponse(fileId, response);
	}
}
